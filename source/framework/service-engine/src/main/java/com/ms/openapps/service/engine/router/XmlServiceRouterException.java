public class XmlServiceRouterException extends Exception {

	private static final long serialVersionUID = 1L;

	public XmlServiceRouterException() {
		super();
	}

	public XmlServiceRouterException(String message, Throwable cause) {
		super(message, cause);
	}

	public XmlServiceRouterException(String s) {
		super(s);
	}

	public XmlServiceRouterException(Throwable cause) {
		super(cause);
	}
}

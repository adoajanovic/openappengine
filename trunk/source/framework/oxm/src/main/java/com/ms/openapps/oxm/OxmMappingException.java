public class OxmMappingException extends Exception {

	private static final long serialVersionUID = 1L;

	public OxmMappingException() {
		super();
	}

	public OxmMappingException(String msg, Throwable t) {
		super(msg, t);
	}

	public OxmMappingException(String msg) {
		super(msg);
	}

	public OxmMappingException(Throwable t) {
		super(t);
	}

}
